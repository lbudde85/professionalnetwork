
var createCookie = function(name, value, days) {
  var date, expires;
  expires = void 0;
  if (days) {
    date = new Date;
    date.setTime(date.getTime() + days * 24 * 60 * 60 * 1000);
    expires = '; expires=' + date.toGMTString();
  } else {
    expires = '';
  }
  return document.cookie = encodeURIComponent(name) + '=' + encodeURIComponent(value) + expires + '; path=/';
};

var readCookie = function(name) {
  var c, ca, i, nameEQ;
  nameEQ = encodeURIComponent(name) + '=';
  ca = document.cookie.split(';');
  i = 0;
  while (i < ca.length) {
    c = ca[i];
    while (c.charAt(0) === ' ') {
      c = c.substring(1, c.length);
    }
    if (c.indexOf(nameEQ) === 0) {
      return decodeURIComponent(c.substring(nameEQ.length, c.length));
    }
    i++;
  }
  return null;
};

var eraseCookie = function(name) {
  return createCookie(name, '', -1);
};

window.sidebarCookie = function(state) {
  if (state) {
    return createCookie('sidebar', 1, 365);
  } else {
    return eraseCookie('sidebar');
  }
};

window.toggleSidebarCookie = function() {
  if (readCookie('sidebar')) {
    return sidebarCookie(0);
  } else {
    return sidebarCookie(1);
  }
};

